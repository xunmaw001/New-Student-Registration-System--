	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				jiaoxueloumingcheng: '',
				jiaoshihao: '',
				zuoweihao: '',
				weizhi: '',
				xuehao: '',
				xueshengxingming: '',
				banji: '',
				userid: '',
				},
